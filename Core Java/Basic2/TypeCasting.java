

class TypeCasting {

    public static void main(String[] args) {
        //There are the two type of the typecasting that were avilabel
        //widning casting , narrowing casting
        //widing casting
        //this is the automatic casting where the casting is perform automatically
        int a = 10 ;
        long b = a ;
        //Narrowing casting
        long c = 20 ;
        int d = (int) c;
        char e = 1 ;
        int f = (int)e;
        float g = 6.6f;
        int h = (int)g;
        System.out.println(h);
    }

}